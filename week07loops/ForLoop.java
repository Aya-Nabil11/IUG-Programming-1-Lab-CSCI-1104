public class ForLoop {
    public static void main(String[] args) {
        /*
        for (initialization; condition; update) {
            // Code to be executed
        }
        
        1 - initialization: Initializes the loop variable. It is executed once at the beginning of the loop.
        2 - condition: Evaluated before each iteration. If true, the loop body executes; if false, the loop terminates.
        3 - execute the loop body
        4 - update: Updates the loop variable after each iteration.
        5 - go to step 2
        */
        
        
        
        
        
        
        
        for(int i =1 ; i<=100 ;i++){
            if(i%2==0){
                System.out.println(i);
            }
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
    }
}
